# tipos de int e float
# int -> Numero interiro
# o tipo int representa qualquer número
# positvo ou negativo. int sem sinal é considerado
# positvo.
print(11) # int
print(-11) # int
print(0)

# obs> CASA DECIMAL EM PROGRAMAÇÃO É SEPARADA POR PONTO (.) 10.32 E Ñ 10,32


# float -> número com ponto flutuante
# o tipo float representa qualquer numero
# posit5ivo ou negativo com ponto fluante.
# float sem sinal é considerado positivo.
print(10.10, 22.32) # float
print(0.0, -0.5)

